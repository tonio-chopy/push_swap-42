void	sb(int *stack_b)
{
	int	temp;
	
	if(!stack_b)
		return ;
	if(stack_b[0] && stack_b[1])
	{
		temp = stack_b[0];
		stack_b[0] = stack_b[1];
		stack_b[1] = temp;
	}
}
