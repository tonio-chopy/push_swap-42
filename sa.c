void	sa(int *stack_a)
{
	int	temp;
	
	if(!stack_a)
		return ;
	if(stack_a[0] && stack_a[1])
	{
		temp = stack_a[0];
		stack_a[0] = stack_a[1];
		stack_a[1] = temp;
	}
}
